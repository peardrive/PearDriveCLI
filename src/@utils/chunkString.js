export function chunkString(str, chunkSize) {
  const chunks = [];
  if (!str) return chunks; // Return empty array if str is empty or undefined
  for (let i = 0; i < str.length; i += chunkSize) {
    chunks.push(str.slice(i, i + chunkSize));
  }
  return chunks;
}
